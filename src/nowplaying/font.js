export const GLYPH_W = 5, GLYPH_H = 7;

/** Return the 7-row glyph for a character (uppercased), or the space glyph if unknown. */
export function glyph(ch) {
  return G[ch] || G[(ch || '').toUpperCase()] || G[' '];
}

/** Pixel width of a string at `scale`, with `gap` scaled columns between glyphs. */
export function textWidth(str, scale = 1, gap = 1) {
  if (!str.length) return 0;
  return str.length * GLYPH_W * scale + (str.length - 1) * gap * scale;
}
